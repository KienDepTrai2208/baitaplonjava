package GameBrickBreaker;

public class Board {
	private int barX;
	private int barY;

	public Board() {
		this.barX = 310;
		this.barY = 510;
	}

	public int getBarX() {
		return barX;
	}

	public void setBarX(int barX) {
		this.barX = barX;
	}

	public int getBarY() {
		return barY;
	}

	public void setBarY(int barY) {
		this.barY = barY;
	}
	
}
